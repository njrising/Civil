function k = localStiffnessMatrix(E,A,I,L)
k = zeros(6,6);
k(1,1) =  A/L; k(1,2) =         0; k(1,3) =        0; k(1,4) = -A/L; k(1,5) =         0; k(1,6) =        0;                                   
k(2,1) =    0; k(2,2) =  12*I/L^3; k(2,3) =  6*I/L^2; k(2,4) =    0; k(2,5) = -12*I/L^3; k(2,6) =  6*I/L^2;
k(3,1) =    0; k(3,2) =   6*I/L^2; k(3,3) =    4*I/L; k(3,4) =    0; k(3,5) =  -6*I/L^2; k(3,6) =    2*I/L;
k(4,1) = -A/L; k(4,2) =         0; k(4,3) =        0; k(4,4) =  A/L; k(4,5) =         0; k(4,6) =        0;
k(5,1) =    0; k(5,2) = -12*I/L^3; k(5,3) = -6*I/L^2; k(5,4) =    0; k(5,5) =  12*I/L^3; k(5,6) = -6*I/L^2;
k(6,1) =    0; k(6,2) =   6*I/L^2; k(6,3) =    2*I/L; k(6,4) =    0; k(6,5) =  -6*I/L^2; k(6,6) =    4*I/L;     
k = E*k;
end